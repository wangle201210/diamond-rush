type idCount struct {
	id    original.RawID
	count int
}

func aggregate(pack *original.WorldPack, layer original.Layer) []idCount {
	counts := map[original.RawID]int{}
	for _, stage := range pack.Stages {
		for id, count := range stage.Histograms[layer] {
			counts[id] += count
		}
	}
	rows := make([]idCount, 0, len(counts))
	for id, count := range counts {
		rows = append(rows, idCount{id: id, count: count})
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].id < rows[j].id })
	return rows
}

func formatKnown(stage *original.Stage, layer original.Layer) string {
	var parts []string
	for _, id := range stage.UniqueIDs(layer) {
		role, ok := original.KnownRole(layer, id)
		if !ok || id == original.EmptyRawID {
			continue
		}
		count := stage.Histograms[layer][id]
		parts = append(parts, fmt.Sprintf("%d %s:%d", id, role.Name, count))
	}
	if len(parts) == 0 {
		return "-"
	}
	if len(parts) > 6 {
		parts = append(parts[:6], fmt.Sprintf("+%d more", len(parts)-6))
	}
	return strings.Join(parts, "<br>")
}

func formatPoints(points []original.Point) string {
	if len(points) == 0 {
		return "-"
	}
	out := make([]string, len(points))
	for i, pt := range points {
		out[i] = fmt.Sprintf("(%d,%d)", pt.X, pt.Y)
	}
	return strings.Join(out, ", ")
}

func fatal(err error) {
	fmt.Fprintln(os.Stderr, "drworldaudit:", err)
	os.Exit(1)
}
