package main

import (
	"flag"
	"fmt"
	"os"
	"sort"
	"strings"

	"github.com/wangle201210/zskc/internal/original"
)

func main() {
	in := flag.String("in", "decoded/world0", "decoded world directory")
	out := flag.String("out", "docs/angkor-world0-inventory.md", "markdown output path")
	flag.Parse()

	pack, err := original.LoadWorldDir(*in)
	if err != nil {
		fatal(err)
	}
	doc := renderMarkdown(pack, *in)
	if err := os.WriteFile(*out, []byte(doc), 0o644); err != nil {
		fatal(err)
	}
	fmt.Printf("wrote %s for %d stages\n", *out, len(pack.Stages))
}

